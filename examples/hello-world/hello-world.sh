# To run the program, put the code in `hello-world.v` and
# use `v run`.
$ v run hello-world.v
hello world

# Sometimes we'll want to build our programs into
# binaries. We can do this using `go build`.
$ go build hello-world.v
$ ls
hello-world	hello-world.v

# We can then execute the built binary directly.
$ ./hello-world
hello world

